    <h2>Etika Bisnis dalam Kehidupan Sehari-hari</h2>

    <h2>1. Artikel Ilmiah Ringan: Bahaya Riba dalam Jual Beli</h2>
    <p>
      Dalam kehidupan sehari-hari, aktivitas jual beli dan transaksi keuangan adalah hal yang lumrah. Namun, dalam praktiknya, tidak semua transaksi dilakukan dengan cara yang sesuai dengan prinsip-prinsip etika, terutama dalam perspektif Islam. Salah satu bentuk pelanggaran yang sering terjadi adalah praktik <strong>riba</strong>.
    </p>
    <p>
      <strong>Riba</strong> adalah penambahan nilai dalam transaksi pinjam-meminjam uang atau dalam jual beli yang tidak dibenarkan oleh syariat Islam. Allah SWT secara tegas mengharamkan riba dalam Al-Qur'an:
    </p>
    <blockquote>
      "Padahal Allah telah menghalalkan jual beli dan mengharamkan riba..." (QS. Al-Baqarah: 275)
    </blockquote>
    <p>
      Praktik riba sering kita temui dalam kehidupan modern, terutama dalam layanan keuangan seperti <em>pinjaman online</em>, <em>kredit konsumtif</em>, dan <em>kartu kredit</em>, di mana bunga dikenakan secara berlipat ganda terhadap peminjam.
    </p>
    <p><strong>Contoh:</strong> Seseorang meminjam uang sebesar Rp2.000.000 dan diwajibkan mengembalikan Rp3.000.000. Tambahan Rp1.000.000 tersebut adalah riba yang sangat memberatkan masyarakat.</p>

    <h3>Dampak Negatif Riba:</h3>
    <ul>
      <li>Merusak keadilan sosial</li>
      <li>Memicu kemiskinan struktural</li>
      <li>Menghilangkan keberkahan harta</li>
    </ul>

